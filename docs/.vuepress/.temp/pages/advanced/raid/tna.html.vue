<h3 id="视奸" tabindex="-1"><a class="header-anchor" href="#视奸"><span>视奸</span></a></h3>
<p>greg生命值降低到66%和33%时会获得90%的抗性，一小段时间后它会进入视奸技能，获得完全免疫伤害。其间greg右手举起一只Sightseeing Interceptor(眼睛)，左手指向一名玩家，同时场地上所有虚空洞都被修复</p>
<p>眼睛会像所有玩家发射激光，先发射两次白色激光，第三次发射火焰激光，火焰激光指向的位置在极短时间内爆炸，爆炸的6格范围内造成80%真伤，8格内50%真伤，无视一切防御和闪避。这一过程持续六次，也就是说需要躲避六个爆炸</p>
<p>爆炸点会出现一个虚空洞，虚空洞中会出现一只名为???的生物喷射虚空块，被砸中也会吃高额伤害</p>
<p>视奸结束后，场地上所有虚空洞再次被修复</p>
<h2 id="技巧" tabindex="-1"><a class="header-anchor" href="#技巧"><span>技巧</span></a></h2>
<p>躲避视奸可以用位移技能或足够高的移速(&gt;135%)，或职业的特殊位移(弓手和刺客的大跳)。由于第三次激光的落点已经确定了爆炸位置，激光出现后就可以跑</p>
<p>如果使用位移躲爆炸，可以每次看到激光时按一下右键，就可以正好在第三次激光后飞走</p>
<p>需要注意的是，你应该尽可能<strong>控制破坏场地的面积</strong>，也就是不要到处跑，搞得满地都是坑，会让队友很难办</p>
