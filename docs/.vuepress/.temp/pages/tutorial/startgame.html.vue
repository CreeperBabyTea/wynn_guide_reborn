<h2 id="账号-职业与存档" tabindex="-1"><a class="header-anchor" href="#账号-职业与存档"><span>账号，职业与存档</span></a></h2>
<p>首先进入Wynncraft，过场动画之后会让你选择职业。这里你需要注意界面上的文字提示，比如滚轮滚动切换职业预览，左键确定选择职业等。</p>
<p>新手第一个职业推荐玩法师(mage)/战士(warrior)，弓手和刺客也可以玩。但是千万别碰萨满，太tm难了</p>
<p>然后讲一下账号、职业与存档的关系</p>
<p>账号就是你的Minecraft账号，一个账号可以有多个游戏存档。每个存档有各自的职业，有不同的数据，如不同的战斗等级、不同的副业等级等。</p>
<p>同一个职业你可以开多个存档，笔者就有两个法师存档分别用来玩球法和风法(这是啥不用管，反正就是两个法师就对了)</p>
<p>随后，选择完存档后你会进到一个新手教程任务，跟着任务做就行。右键npc对话，对话的过程中你会被保护，周围的怪不会打你，仇恨也会丢失</p>
<h2 id="新手村ragni" tabindex="-1"><a class="header-anchor" href="#新手村ragni"><span>新手村Ragni</span></a></h2>
<p>ok，完成教程任务你就来到了新手村Ragni，Wynn大陆的首都，也是Wynn第二强大的要塞。现在按下m打开地图，你会看到地图上有很多城镇，比如西面Ragni，西南面的Troms，中央的Detlas。</p>
<p>城镇里面<strong>一般</strong>不会刷怪，是玩家的聚集地。</p>
<p>走出王宫，左前方的是Bank。Bank里的末影箱里可以存放你的物品。同时Emerald Trader允许你将64个绿宝石合并城一个绿宝石块</p>
