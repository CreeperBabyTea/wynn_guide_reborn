<h2 id="副业系统" tabindex="-1"><a class="header-anchor" href="#副业系统"><span>副业系统</span></a></h2>
<p>在Ragni外面你可能会注意到树上、矿石、水中有文字提示你采集它，采集这些资源需要特别的工具。工具可以从路边npc买到，但是不推荐新手涉足副业。</p>
<p>副业包括采集与制造，成本都非常高昂，是后期组装毕业配装时才考虑的问题。</p>
<p>在<a href="https://wynncraft.wiki.gg/wiki/Cook_Assistant" target="_blank" rel="noopener noreferrer">Cook Assistant (level 3)</a>中，你将体验到采集系统</p>
<h2 id="头上有前缀的玩家" tabindex="-1"><a class="header-anchor" href="#头上有前缀的玩家"><span>头上有前缀的玩家</span></a></h2>
<p>你可能已经发现了，你在探险路上遇到了很多ID前缀带有<code v-pre>[NA/EU/AS XX]</code>的半透明玩家</p>
<p>他们其实都是来自其他服务器的玩家，对你来说相当于虚影</p>
<blockquote>
<p>其他服务器，意思是Wynn的其他子服，比如亚服、北美服、欧服。你可以使用/hub指令，在大厅用指南针右键菜单选择你的服务器</p>
</blockquote>
<p>但是如果你们都能彼此看到对方的话，是可以直接进行对话交流的</p>
<p>虚影玩家可能会对你的延迟造成影响，如果你觉得卡顿的话，不妨使用<code v-pre>/toggle ghosts none</code>来关闭虚影玩家</p>
<p>想要重新开启，只需要输入<code v-pre>/toggle ghosts all/high/medium/low</code>即可</p>
<p>查看你当前所在的服务器，只需要按下Tab，在第二栏就可以找到</p>
